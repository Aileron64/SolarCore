    void Update()
    {

        if (Input.GetMouseButtonDown(2))
        {
            TextManager.Instance.DebugText("Shake");

            ProCamera2DShake.Instance.Shake("Beat");
        }
            

        if (Input.GetKeyDown(KeyCode.Alpha0))
        {
            DebugText.Instance.SetText("( ͡° ͜ʖ ͡°) CHEATER ( ͡° ͜ʖ ͡°)");

            Music.Instance.GetComponent<AudioSource>().Stop();

            LevelComplete();
        }

        if (Input.GetKeyDown(KeyCode.Alpha1))
        {
            //Camera.main.backgroundColor = color[4];

            //Camera.main.DOColor(color[1], 0.5f);

            //backgroundMat[2].DOColor(_color, 0.45f);

            background.ChangeColour(color[0]);
        }


        if (Input.GetKeyDown(KeyCode.Alpha2))
            background.ChangeColour(color[1]);

        if (Input.GetKeyDown(KeyCode.Alpha3))
            background.ChangeColour(color[2]);

        if (Input.GetKeyDown(KeyCode.Alpha4))
            background.ChangeColour(color[3]);

        if (Input.GetKeyDown(KeyCode.Alpha5))
            background.ChangeColour(color[4]);


        if (Input.GetKeyDown(KeyCode.Alpha6))
            background.DiscoTime();

        if (Input.GetKeyDown(KeyCode.Alpha9))
        {
            if (flag)
                background.ToggleStars(true);
            else
                background.ToggleStars(false);

            flag = !flag;
        }
    }





    protected override void Spawn(int waveNum)
    {
        if(!CheckForEnemies(testPool) && spawnEnemys)
        {
            SpawnCircle(testPool, numPerWave, spawnRadius);

            if (_spawnCoins)
                SpawnCoinCircle(6, Random.Range(300, 1300));


        }
    }

    bool CheckForEnemies(List<GameObject> enemyPool)
    {
        for (int i = 0; i < enemyPool.Count; i++)
        {
            if (enemyPool[i].activeSelf)
            {
                return true;
            }
        }

        return false;
    }
        



